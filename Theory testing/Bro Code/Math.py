import math 

pi = 3.14
j,k,l = 30,70,29
print(round(pi)) #round the number
print(math.ceil(pi)) # round up the number
print(math.floor(pi)) #round down the number
print(math.sqrt(9)) #Square root #ex: square root of 9 = 3
print(abs(pi)) # Absolute value of the number(Original value)
print(pow(pi,2))   # power pi,2 = pi**2
print(max(pi,j,k,l)) #max value
print(min(pi,j,k,l)) #min value