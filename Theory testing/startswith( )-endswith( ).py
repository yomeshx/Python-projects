#Startswith
line = "Depression Kills"

if line.startswith("Depression"):
    print("YES, Sentence starts with 'Depression'")
else:
    print("No,  Sentence doesn't starts with 'Depression'")

#Endswith
line = "Depression Kills"

if line.endswith("Kills"):
    print("Yes ,Sentence ends with 'Kills'")
else:
    print("No ,Sentence doesn't ends with 'Kills'")