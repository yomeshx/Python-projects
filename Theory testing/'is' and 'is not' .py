


x = None

print("Only use 'is and is not' when you dealing with boolean and None")
print(f"\nCurrent Value is {x}\n")

print("Using 'is' |||| if x is None it will print AAAAAAAAAAAAAAAAA , if x isn't none it will print BBBBBBBBBBBBBBBBBB")

if x is None :
    print("\n\nAAAAAAAAAAAAAAAAA\n\n")

else :
    print("\n\nBBBBBBBBBBBBBBBBB\n\n")



print("Using 'is not' |||| if x isn't None , it will print CCCCCCCCCCCCCCCCCC,\n if it is None it will print DDDDDDDDDDDDDDDDDDDDD")


if x is not None :
    print("\n\nCCCCCCCCCCCCCCCCCCCC\n\n")

else :
    print("\n\nDDDDDDDDDDDDDDDDDDDD\n\n")

