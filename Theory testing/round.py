#Round is use for rounding up a number
#mostly used when dealing with floats

print(round(4.6))#this will round up to closest int number in this case
#you can also decide how many digits to round for by specifying no of digits
print(round(5.66666,3))#this will round up to closest with 3 floats

x = 4.7777
print(f"{x:.2f}")#same thing but specialized syntax ':.2f' means 2 digits in the end
print(f"{x:.3f}")#same thing but specialized syntax ':.3f' means 3 digits in the end

